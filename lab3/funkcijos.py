# Globalus splainas

# randa delta x masyva
def getDistance(x):
    n = len(x)
    d = np.zeros(n - 1)

    for i in range(n - 1):
        d[i] = x[i + 1] - x[i]
    return d


# f'' masyvas
def get_F_values(x, y):
    n = len(x)
    # x reikšmės
    A = np.zeros((n, n))
    # koeficientai
    b = np.zeros(n)
    # greta esančių taškų atstumai
    d = getDistance(x)

    # 40 skaidrė
    for i in range(n - 2): # Nustatoma istrizaine dalis
        A[i, i] = d[i] / 6
        A[i, i + 1] = (d[i] + d[i + 1]) / 3
        A[i, i + 2] = d[i + 1] / 6

    # 40 skaidrė
    for i in range(n - 2): # Nustatoma y matrica
        b[i] = ((y[i + 2] - y[i + 1]) / d[i + 1]) - ((y[i + 1] - y[i]) / d[i])

    # 43 skaidrė
    # Nustatomi apacio kampai A matricai
    A[n - 2, 0] = d[0] / 3
    A[n - 2, 1] = d[0] / 6
    A[n - 1, 0] = 1

    A[n - 2, n - 2] = d[n - 2] / 6
    A[n - 2, n - 1] = d[n - 2] / 3
    A[n - 1, n - 1] = -1

    # Nustatomi y matricos apacia
    b[n - 2] = ((y[1] - y[0]) / d[0]) - ((y[n - 1] - y[n - 2]) / d[n - 2])

    ff = np.linalg.solve(A, b)

    return ff


# skaiciuoja funkcijos reiksme taske, 44 skaidre
def splinify(ff1, ff2, s, d, y1, y2):
    a = (ff1 * (s**2 / 2)) - (ff1 * (s**3 / (6*d)))
    b = (ff2 * (s**3 / (6*d))) + (((y2 - y1) / d) * s)
    c = (ff1 * ((d/3) * s)) + (ff2 * ((d/6) * s))

    return a + b - c + y1

