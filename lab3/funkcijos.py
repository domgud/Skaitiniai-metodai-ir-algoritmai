# Aproksimavimas

def G_matrix(x_points, degree):
  n = len(x_points)
  G = np.zeros((n,degree))

  for i in range(0, n):
    for j in range(0, degree):
      G[i][j] = x_points[i]**j

  return G

def get_y_points(x, c):
    n = len(c)
    result = 0

    for i in range(0, n):
        result += c[i]*(x**i)
    return result

def draw_graph(x,y, daugianaris):
    G = G_matrix(x, daugianaris)
    A = np.matmul(G.transpose(), G)
    b = np.matmul(G.transpose(), y)
    c = np.linalg.inv(A).dot(b)

    xx = np.linspace(1, 12, 100)
    yy = get_y_points(xx, c)

    plt.plot(xx, yy, label='Aproksimuota linija daugiariu '+ str(daugianaris))


