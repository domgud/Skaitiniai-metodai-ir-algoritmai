# Itemptas splainas

def get_F_values_itemptas(x, y, sg):
    n = len(x)
    # x reikšmės
    A = np.zeros((n, n))
    # koeficientai
    b = np.zeros(n)
    # greta esančių taškų atstumai
    d = getDistance(x)

    # 40 skaidrė
    for i in range(n - 2): # Nustatoma istrizaine dalis
        sg1 = sg[i]
        sg2 = sg[i+1]

        A[i, i] = 1 / (sg1**2 * d[i]) - 1 / (sg1* np.sinh(sg1*d[i]))
        A[i, i + 1] = np.cosh(sg1*d[i]) / (sg1 * np.sinh(sg1*d[i])) + np.cosh(sg2 * d[i+1]) / (sg2 * np.sinh(sg2 * d[i+1])) - 1/(sg1**2 * d[i]) - 1/(sg2**2 * d[i+1])
        A[i, i + 2] = 1 / (sg2**2 * d[i+1]) - 1 / (sg2 * np.sinh(sg2 * d[i+1]))

    # 40 skaidrė
    for i in range(n - 2): # Nustatoma y matrica
        b[i] = ((y[i + 2] - y[i + 1]) / d[i + 1]) - ((y[i + 1] - y[i]) / d[i])

    # 43 skaidrė
    # Nustatomi apacio kampai A matricai
    A[n - 2, 0] = 1
    A[n - 1, n - 1] = 1
    # Nustatomi y matricos apacia
    b[n - 1] = 0
    b[n - 2] = 0

    ff = np.linalg.solve(A, b)

    return ff

def get_new_y_itemptas(ff1, ff2, s, d, y1, y2, sg):
    a = (ff1 / sg**2) * np.sinh(sg * (d - s)) / np.sinh(sg*d)
    b = (y1 - (ff1 / sg**2)) * ((d - s)/d)
    c = (ff2 / sg**2) * np.sinh(sg*s)/np.sinh(sg*d)
    h = (y2 - ff2 / sg**2)* s / d

    return a + b + c + h



